package pp;

public class Student {
private int sid;
private String sname;
private Trainer tr;
public int getSid() {
	return sid;
}
public void setSid(int sid) {
	this.sid = sid;
}
public String getSname() {
	return sname;
}
public void setSname(String sname) {
	this.sname = sname;
}
public Trainer getTr() {
	return tr;
}
public void setTr(Trainer tr) {
	this.tr = tr;
}

}
