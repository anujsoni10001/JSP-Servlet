package pp;

public class Address {
private int hno;
private String city;
private Emp employee;
public int getHno() {
	return hno;
}
public void setHno(int hno) {
	this.hno = hno;
}
public String getCity() {
	return city;
}
public void setCity(String city) {
	this.city = city;
}
public Emp getEmployee() {
	return employee;
}
public void setEmployee(Emp employee) {
	this.employee = employee;
}

}
