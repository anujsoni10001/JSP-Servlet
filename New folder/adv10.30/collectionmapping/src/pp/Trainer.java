package pp;

import java.util.List;

public class Trainer {
private int id;
private String name;
private List sname;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public List getSname() {
	return sname;
}
public void setSname(List sname) {
	this.sname = sname;
}

}
