package pp;

public class Student {
private int sid;
private String sname;
private String course;
private Trainer tname;
public int getSid() {
	return sid;
}
public void setSid(int sid) {
	this.sid = sid;
}
public String getSname() {
	return sname;
}
public void setSname(String sname) {
	this.sname = sname;
}
public String getCourse() {
	return course;
}
public void setCourse(String course) {
	this.course = course;
}
public Trainer getTname() {
	return tname;
}
public void setTname(Trainer tname) {
	this.tname = tname;
}

}
