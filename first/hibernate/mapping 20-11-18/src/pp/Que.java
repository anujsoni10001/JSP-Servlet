package pp;

import java.util.Set;

public class Que {
private int id;
private String ques;
private Set<String> ans;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getQues() {
	return ques;
}
public void setQues(String ques) {
	this.ques = ques;
}
public Set<String> getAns() {
	return ans;
}
public void setAns(Set<String> ans) {
	this.ans = ans;
}

}
